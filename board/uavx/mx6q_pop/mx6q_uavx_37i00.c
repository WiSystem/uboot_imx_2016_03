static void setup_spi(void)
{
	u32 reg;
	struct mxc_ccm_reg *ccm = (struct mxc_ccm_reg *)CCM_BASE_ADDR;

	// Enable ECSPI1 clocks
	reg = readl(&ccm->CCGR1);
	reg |= 0x03;
	writel(reg, &ccm->CCGR1);

	imx_iomux_v3_setup_multiple_pads(ecspi1_pads, ARRAY_SIZE(ecspi1_pads));
}

